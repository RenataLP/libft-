#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	size_t s;
	size_t i;

	s = 0;
	i = 0;
	if (!s1 || !s2)
		return (0);
	while ((s1[i] != '\0') || (s2[i] != '\0'))
	{
		s = s1[i] - s2[i];
		if (s != 0)
			return (0);
		i++;
	}
	return (1);
}
